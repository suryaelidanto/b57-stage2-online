import { User } from "../types/user";

interface UserListProps {
  users: User[];
}

export function UserList({ users }: UserListProps) {
  return (
    <>
      {users.map((user) => (
        <div
          style={{
            backgroundColor: "orangered",
            color: "white",
          }}
        >
          <h1>{user.fullName}</h1>
          <h1>{user.address}</h1>
          <h1>{user.profilePicture}</h1>
        </div>
      ))}
    </>
  );
}
